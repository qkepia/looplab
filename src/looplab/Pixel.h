#ifndef LOOPLAB_PIXEL_H
#define LOOPLAB_PIXEL_H

#include <cstdint>

//------------------------------------------------------------------------------------------------//

namespace looplab {

//------------------------------------------------------------------------------------------------//
// RGB
//------------------------------------------------------------------------------------------------//

template<typename T>
struct RGB
{
	T r;
	T g;
	T b;
};

using RGB8 = RGB<uint8_t>;
using RGB16 = RGB<uint16_t>;
using RGBf = RGB<float>;

//------------------------------------------------------------------------------------------------//
// RGBA
//------------------------------------------------------------------------------------------------//

template<typename T>
struct RGBA
{
	T r;
	T g;
	T b;
	T a;
};

using RGBA8 = RGBA<uint8_t>;
using RGBA16 = RGBA<uint16_t>;
using RGBAf = RGBA<float>;

//------------------------------------------------------------------------------------------------//

} // namespace looplab

//------------------------------------------------------------------------------------------------//

#endif